package com.example.quizbanglaia1.TrangChu;

public class classHome {
    private int Hinh;
    private String MoTa;

    public classHome(int hinh, String moTa) {
        this.Hinh = hinh;
        this.MoTa = moTa;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }

    public String getMoTa() {
        return MoTa;
    }

    public void setMoTa(String moTa) {
        MoTa = moTa;
    }
}
